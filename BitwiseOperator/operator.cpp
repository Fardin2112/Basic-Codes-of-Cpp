#include<bits/stdc++.h>
using namespace std;

// ^ symbol or XOR gate
// ~ NOT gate
// & AND gate
// | OR gate
int main (){
    int a = 2;
    int b = 6;

    cout<<" a & b = "<<(a & b)<<endl;
    cout<<" a | b = "<<(a | b)<<endl;
    cout<<"~a = " << ~a <<endl;
    cout<<" a ^ b = "<<(a ^ b)<<endl;

    return 0;
}